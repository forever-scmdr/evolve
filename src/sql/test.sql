SELECT DISTINCT 
	ITEM.*, PARAMS.* , PARENT.IP_PARENT_ID AS PRED_ID 
FROM 
	Item AS ITEM, ItemParameters_enterprise_common_enterprise AS PARAMS , ItemParent AS PARENT  
WHERE 
	ITEM.I_TYPE_NAME IN ('enterprise', 'common_enterprise') 
	AND ITEM.I_REF_ID = PARAMS.IR_REF_ID  
	AND PARENT.IP_PARENT_ID IN (748, 773, 794, 808, 859, 872, 913, 949, 970, 1004, 1046, 1084, 1266, 1288, 1302) 
	AND PARENT.IP_REF_ID = ITEM.I_ID    
	AND ( PARAMS.enterprise_country = 'Беларусь')  
	ORDER BY  ITEM.I_CHILD_INDEX